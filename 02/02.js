function plus(a,b){
    let answer;
    answer = a + b;
    return answer;
}

function minus(c,d){
    let answer;
    answer = c - d;
    return answer;
}

function times(e,f){
    let answer;
    answer = e * f;
    return answer;
}

function div(g,h){
    let answer;
    answer = g / h;
    return answer;
}

function mod(i,j){  
    let answer;
    answer = i % j;
    return answer;
}
